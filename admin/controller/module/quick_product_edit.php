		$data['cancel'] = $this->url->link('extension/module', 'token=' . $this->session->data['token'], 'SSL');

		if (isset($this->request->post['quick_product_edit_status'])) {
			$data['quick_product_edit_status'] = $this->request->post['quick_product_edit_status'];
		} else {
			$data['quick_product_edit_status'] = $this->config->get('quick_product_edit_status');
		}
		
		if (isset($this->request->post['quick_product_edit_tabs'])) {
			$data['quick_product_edit_tabs'] = $this->request->post['quick_product_edit_tabs'];
		} else {
